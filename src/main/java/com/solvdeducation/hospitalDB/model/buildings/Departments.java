package com.solvdeducation.hospitalDB.model.buildings;

public class Departments {
    private long id;
    private String name;
    private String head;

    public Departments() {
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHead() {
        return head;
    }

    public void setHead(String head) {
        this.head = head;
    }
}
